private void sokProjektDatum() {
    String start = jFormattedTextField1.getText();
    String slut = jFormattedTextField2.getText();

    // Validera datum
    if (!start.matches("\\d{4}-\\d{2}-\\d{2}") || !slut.matches("\\d{4}-\\d{2}-\\d{2}")) {
        JOptionPane.showMessageDialog(this, "Ange datum i formatet YYYY-MM-DD.");
        return;
    }

    try {
        String sql = "SELECT pid, projektnamn, startdatum, slutdatum " +
                     "FROM projekt " +
                     "WHERE startdatum >= '" + start + "' " +
                     "AND slutdatum <= '" + slut + "' " +
                     "AND aid = '" + aid + "'";

        ArrayList<HashMap<String, String>> projekt = idb.fetchRows(sql);

        DefaultTableModel modell = (DefaultTableModel) jTable1.getModel();
        modell.setRowCount(0); // TÃ¶m tidigare rader

        if (projekt != null) {
            for (HashMap<String, String> rad : projekt) {
                Object[] radData = {
                    rad.get("PID"),
                    rad.get("PROJEKTNAMN"),
                    rad.get("STARTDATUM"),
                    rad.get("SLUTDATUM")
                };
                modell.addRow(radData);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Inga projekt hittades.");
        }
    } catch (InfException e) {
        JOptionPane.showMessageDialog(this, "Fel vid databasanrop: " + e.getMessage());
    }
}
