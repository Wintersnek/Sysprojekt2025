    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String selectedUser= (String) jComboBox1.getSelectedItem();
        
        if (selectedUser != null && !selectedUser.isEmpty()) {
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Är du säker på att du vill ta bort " + selectedUser + " från systemet?",
            "Bekräfta borttagning",
            javax.swing.JOptionPane.YES_NO_OPTION
        );
        
        if (confirm == javax.swing.JOptionPane.YES_NO_OPTION) {
            deleteUser(selectedUser);
        }
      }  else {
            javax.swing.JOptionPane.showMessageDialog(this, "Välj en användare att ta bort.");
        }
            // Säkerställer att användare vill ta bort projektet.
    }//GEN-LAST:event_jButton2ActionPerformed

