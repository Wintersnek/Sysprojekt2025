private void deleteUser(String selectedUser) {
        try {
            String[] parts = selectedUser.split(" ");
            String fornamn = parts[0];
            String efternamn = parts[1];

            String deleteQuery = "DELETE FROM anstalld WHERE fornamn = '" + fornamn + "' AND efternamn = '" + efternamn + "'";
            idb.delete(deleteQuery);

            javax.swing.JOptionPane.showMessageDialog(this, selectedUser + " har tagits bort från systemet.");
            jComboBox1.removeItem(selectedUser);
        } catch (InfException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Fel vid borttagning av användare: " + e.getMessage());
            e.printStackTrace();
        }
}

