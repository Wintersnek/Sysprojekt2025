    private void staffComboBox() {
        try {
            String query ="SELECT aid, fornamn, efternamn FROM anstalld";
            ArrayList<HashMap<String, String>> users = idb.fetchRows(query);
            
            if (users != null) {
                for (HashMap<String, String> user : users){
                    String name = user.get("aid") + ". " + user.get("fornamn") + " " + user.get("efternamn");
                    jComboBox1.addItem(name);
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Inga användare hittades i systemet.");
            }
            
        } catch (InfException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Fel vid hämtning av användare: " + e.getMessage());
        e.printStackTrace();
    }
    }


