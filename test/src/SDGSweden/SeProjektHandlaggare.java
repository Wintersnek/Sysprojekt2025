    private void visaProjektForHandlaggare() {
        try {
            String sql = "SELECT p.pid, p.projektnamn, p.startdatum, p.slutdatum, p.status " +
                         "FROM projekt p JOIN ans_proj ap ON p.pid = ap.pid " +
                         "WHERE ap.aid = " + aid + "";
            
            ArrayList<HashMap<String, String>> projekt = idb.fetchRows(sql);

            DefaultTableModel modell = (DefaultTableModel) tblProjekt.getModel();
            modell.setRowCount(0);
            
            if (projekt != null) {
                for (HashMap<String, String> rad : projekt) {
                    String pid = rad.get("pid");
                    String namn = rad.get("projektnamn");
                    String start = rad.get("startdatum");
                    String slut = rad.get("slutdatum");
                    String status = rad.get("status");
                    
                    Object[] radData = { pid, namn, start, slut, status };
                    modell.addRow(radData);
                    }
            }
            
            int funnaProjekt = projekt.size();
            if (funnaProjekt <= 0)
            {
                JOptionPane.showMessageDialog(null, "Inga projekt funna!");
            }
        } catch (InfException e) {
            System.err.println("Fel vid hÃ¤mtning av projekt: " + e.getMessage());
        }
    }

