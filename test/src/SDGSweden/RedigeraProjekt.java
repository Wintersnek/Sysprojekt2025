    private void PopuleraProjekt() {
        try {

            String sqlFraga = "SELECT pid, projektnamn, beskrivning, startdatum, "
                    + "slutdatum, kostnad, status, prioritet, projektchef, land FROM projekt WHERE pid = '" + pid + "'";

            HashMap<String, String> projektInfo = idb.fetchRow(sqlFraga);

            if (projektInfo != null) {
                jTextFieldProjektnamn.setText(projektInfo.get("projektnamn"));
                jTextFieldBeskrivning.setText(projektInfo.get("beskrivning"));
                jTextFieldStartdatum.setText(projektInfo.get("startdatum"));
                jTextFieldSlutdatum.setText(projektInfo.get("slutdatum"));
                jTextFieldKostnad.setText(projektInfo.get("kostnad"));
                jTextFieldStatus.setText(projektInfo.get("status"));
                jTextFieldPrioritet.setText(projektInfo.get("prioritet"));
                jTextFieldProjektchef.setText(projektInfo.get("projektchef"));
                jTextFieldLand.setText(projektInfo.get("land"));
            }

        } catch (InfException e) {
            JOptionPane.showMessageDialog(null, "Fel vid h√§mtning av projekt: " + e.getMessage());
        }
    }
    
    private void sparaProjekt() {
        try {
            String sql = "UPDATE projekt SET " +
                    "projektnamn = '" + tfNamn.getText() + "', " +
                    "beskrivning = '" + tfBeskrivning.getText() + "', " +
                    "startdatum = '" + tfStart.getText() + "', " +
                    "slutdatum = '" + tfSlut.getText() + "', " +
                    "kostnad = '" + tfKostnad.getText() + "', " +
                    "status = '" + tfStatus.getText() + "', " +
                    "prioritet = '" + tfPrioritet.getText() + "', " +
                    "projektchef = '" + tfProjektchef.getText() + "', " +
                    "land = '" + tfLand.getText() + "' " +
                    "WHERE pid = " + pid;

            idb.update(sql);
            JOptionPane.showMessageDialog(this, "Projekt uppdaterat!");
            dispose();
        } catch (InfException e) {
            JOptionPane.showMessageDialog(this, "Fel: " + e.getMessage());
        }
    }

